package com.nasalevich.epampracticefirst


fun sum(firstValue: Double, secondValue: Double): String {
    return (firstValue + secondValue).toString()
}

fun subtraction(firstValue: Double, secondValue: Double): String {
    return (firstValue - secondValue).toString()
}

fun multiplication(firstValue: Double, secondValue: Double): String {
    return (firstValue * secondValue).toString()
}

fun division(firstValue: Double, secondValue: Double): String {
    return (firstValue / secondValue).toString()
}